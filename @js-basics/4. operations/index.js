// + - * %

// += -= *= %=
let number = 3;
number += 3; // number = number + 3
console.log(number);

// > <

// == ===
const num = 2;
const string = "2";

console.log(num == string);
console.log(num === string);

// != !==
console.log(num != string);
console.log(num !== string);

// && ||
const number1 = 2;
const number2 = 3;
console.log(number1 === 2 || number2 === 4);
console.log(number1 === 2 && number2 === 4);

// ++ --
let i = 1;
console.log(i++);
console.log(i);

let j = 1;
console.log(++j);
console.log(j);
